                            <p class="lead-text">
                            As businesses worldwide look to be nimbler, reduce risks and tap into wider networks
                             of expertise, it is no wonder that the trend towards outsourcing has exploded. 
                             Outsourcing, the transfer of work to an external company, is nothing new. But 
                             recent years have seen an exponential increase in this practice, and this is 
                             expected to continue.
                            </p>

                            <p class="lead-text">
                            To many people, outsourcing is a frightening proposition. Yet this new business 
                            model, which has been adopted worldwide across both the private and the public 
                            sectors, provides multiple benefits. It enables an organization to achieve business 
                            objectives, add value, tap into a resource base, and mitigate risk. In other words, 
                            from individual items all the way to systems management, choosing to use external 
                            providers allows the company or organization outsourcing a job (the “client”) to 
                            focus on what it does best.
                            </p>

                            <p class="lead-text">
                            Outsourcing is a good business strategy. It improves efficiency, cuts cost, speeds 
                            up product development, and allows companies to focus on their “core competencies”. 
                            And with help from a new ISO standard, the future is looking bright. 
                            </p>

                            <p class="lead-text">
                            ISO 37500 addresses issues of flexibility in outsourcing arrangements, accommodating 
                            changing business requirements. The risks involved in outsourcing are confronted to 
                            enable mutually beneficial collaborative relationships. The standard is intended to 
                            relate to any outsourcing relationship, whether outsourcing for the first time or 
                            not, using a single-provider or multi-provider model, or draft agreements based on 
                            services or outcomes. 
                            </p>

                            <p class="lead-text">
                            According to Bannink, <a href="javascript:void">ISO 37500</a>, which focuses on 
                            business relations, especially business-to-business transactions, will help 
                            organizations put in place an adaptive business model to enable successful 
                            outsourcing interactions. 
                            </p>
                            <p class="lead-text">
                            In addition, it can be tailored and extended to industry-specific needs to 
                            accommodate international, national, and local laws and regulations (including 
                            those related to the environment, labor, health and safety), the size of the 
                            outsourcing arrangement and the type of industry sector.
                            </p>
                            <p class="lead-text">
                            The economies of scale, financial rewards, the flexibility, and increased 
