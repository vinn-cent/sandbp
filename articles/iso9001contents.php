                            when bidding for contracts, an ISO 9001 certification is commonly a requirement.
                            <p class="lead-text">
                            It was shown in a recent study by the American Society for Quality, that every $1 invested into your QMS 
                            results in an average increase of $6 in revenue, $3 in profits, and a reduction of $16 in QMS costs.
