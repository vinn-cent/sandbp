                            informally use the standard in one way or another.
                            </p>

                            <p class="lead-text">
                            As an international standard, ISO 9001 specifies the requirements for organizations that want to ensure 
                            continual improvement and meet customers’ needs. 
                            </p>
                            
                            <p class="lead-text">
                            ISO 9001 was specifically designed to serve as a guiding framework for organizations of all sizes and 
