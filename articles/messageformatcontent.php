                            ISO 8583 is an international standard for financial transaction card originated interchange messaging. It is the International Organization for Standardization standard for systems that exchange electronic transactions initiated by cardholders using payment cards.
                            </p>

                            <p class="lead-text">
                            ISO 8583 defines a message format and a communication flow so that different systems can exchange these transaction requests and responses.
                            Although ISO 8583 defines a common standard, it is not typically used directly by systems or networks. It defines many standard fields (data elements) which remain the same in all systems or networks and leaves a few additional fields for passing network-specific details. 
                            These fields are used by each network to adapt the standard for its use with custom fields and custom usages.</p>

