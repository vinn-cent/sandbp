                        <div class="heading-wrapper with-separator">
                            <h2 class="h1">
                                <span>ISO 30301:</span>
                                Records management as an information management discipline
                            </h2>
                        </div>
                        <div style="margin-top: -8%;">
                            <img src="./assets/pictures/article1.jpg" alt="Records management as an information management discipline">
                        </div>
                        <div>
                            <p class="lead-text">
                            Records information management is regarded as one of the pillars of any 
                            organization because the organization’s activities are based on access to the 
                            information contained in records.
                            </p>

                            <p class="lead-text">
                            This article discusses records management within the context of information 
                            management as a whole. Information management is the process of identifying, 
                            controlling, accessing, searching, and retrieving information centrally and 
                            ensuring that this information is accessible to all members of the organization.
                            </p>

                            <p class="lead-text">
                            Information management includes records management. The fact that the information 
                            management function includes records management is confirmed by the draft 
                            unified standard on document management issued by the American National Standards 
                            Institute (ANSI). ANSI is a world-renowned organization, and its standards are 
                            used in the business sector and in the government of many countries of the world. 
                            </p>

                            <p class="lead-text">
                            The draft unified standard on document management proposes that organizations 
                            classify documents into two major categories: records and non-records. but is 
                            not limited to it. However, records are an essential component of information 
                            assets and therefore they are very much part of information management. The 
                            method by which records are created may be manual or automated. They may be 
                            kept in hard-copy form or in electronic format as e-records. 
                            </p>

                            <p class="lead-text">
                            This article will approach records information management from the point of view 
                            of information management and not in terms of a specific subset or sector of 
                            information management which is records. This is based on the assumption that 
                            records must be managed to ensure that an organization’s activities are centered 
                            around the use and access to the relevant, accurate and valid information in a 
                            timely manner. 
                            </p>
                            <p class="lead-text">
                            Records management is often seen as an activity that falls within the scope of 
                            recordkeeping. However, records management is not limited to recordkeeping. 
                            This article will discuss records management as a discipline that is distinct 
                            from recordkeeping.
                            <br>
                            Records management can be clearly defined as separate from the information 
                            management function, records are information that pertains to an organization 
                            and its operations. However, it does not include the information processing function 
                            or any other subset of information management. An organization’s records may 
                            contain information for which there are no employees or relevant members in the 
                            organization because this information is too sensitive, confidential, or proprietary.
                            </p>
                            <p class="lead-text">
                            Governance, risk management, and other data storage practices are a combination 
                            of technology deployment and policy development. Your organization needs to 
                            have a variety of workflows and safeguards in place to ensure that you are 
                            keeping data and internal information secure and compliant.
                            </p>
                            <p class="lead-text">
                            <a href="https://sandbp.net/articles/ISO-30301-record-management-as-an-information-discipline">ISO 30301</a> 
                            specifies requirements to be met by a management system for records (MSR) to 
                            support an organization in the achievement of its mandate, mission, strategy, and goals. 
                            It addresses the development and implementation of a records policy and 
                            objectives and gives information on measuring and monitoring performance.
                            <br>
                            An MSR can be established by an organization or across organizations that share 
                            business activities. 
                            </p>
                            <p class="lead-text">
                            Records are an organization's institutional memory. They are essential tools to 
                            help people across the organization conduct their daily work effectively, 
                            equitably, and efficiently. Your efforts to manage your information, documents, 
                            and records properly will ensure the operations of the organization is 
                            successful as possible and will help preserve valuable evidence of the 
                            organization’s vitally important work.
                            </p>
