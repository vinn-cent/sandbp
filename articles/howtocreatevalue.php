                        <div class="heading-wrapper with-separator">
                            <h2 class="h1">
                                How Can You Create Value from Your Asset -
                                <span>ISO 55001 </span> Can Help!
                            </h2>
                        </div>
                        <div style="margin-top: -8%;">
                            <img src="./assets/pictures/ISO-55001-Asset-Management-Systems-Certification.jpg" alt="Records management as an information management discipline">
                        </div>
                        <div>
                            <p class="lead-text">
                            What links a brand's reputation, a telecommunications network and a Skycrapper? 
                            While these things may seem very different, according to experts working <a href="javascript:void">ISO 55001</a>, they can all be seen as assets creating value for a company and can 
                            therefore be managed in similar ways.
                            </p>

                            <p class="lead-text">
                            <b>
                            So, what is an asset and why would someone need to manage it?
                            </b> <br>
                            Well, <a href="javascript:void">ISO 55001</a> defined an asset as an item, thing or entity that has potential 
                            or actual value for an organization. This is vague, but in fact purposefully so. 
                            We need to understand that an asset can be anything from tangible and physical 
                            items such as rails, trains, and vehicles to the more intangible such as the 
                            reputation of a company.
                            </p>

                            <p class="lead-text">
                            All these things can bring value to a company and need to be well managed in 
                            order to make the most of that value.
                            </p>

                            <p class="lead-text">
                            <b>
                            These are quite different things, is managing vehicles really the same as 
                            managing a brand?
                            </b>
                            <br>
                            There are many similarities yes, and the fundamental principles are the same. 
                            If you do nothing with things, and this applies to both a brand and vehicles, 
                            they deteriorate. Their value, or potential value, decreases. All assets need 
                            maintaining so although the actions we take to maintain them might be different 
                            (e.g., for vehicles, this might be periodical maintenance, whereas for brands 
                            it might be choosing to sponsor new events), both will benefit from long term 
                            plans and strategies.
                            </p>

                            <p class="lead-text">
                            Asset management is about knowing what we want to achieve with an asset and how 
                            to make it happen, in addition to assessing risks associated with that asset. 
                            It is about having a long-term strategy.
                            </p>

                            <p class="lead-text">
                            <b>
                            Most successful organizations and companies have long term strategies, with 
                            yearly objectives and so on. Why do we need a strategic asset management plan 
                            as well?
                            </b>
                            <br>
                            One of the key things with assets is that their life span can be much longer, 
                            or much shorter, than the average strategic plan. A brand's reputation will 
                            (hopefully!) outlast a five-year plan, as will the physical infrastructure of a 
                            vehicle for example, so the long-term strategic asset management plan has to 
                            take this longer life span into account and plan for it.
                            </p>
                            <p class="lead-text">
                            We may not always be aware of everything that has value or has the potential to 
                            create value for our organization. Identifying assets, what we want to achieve 
                            with them and how to get there, requires in-depth knowledge of the asset in 
                            question, which can help in operational decision making and an organization’s 
                            performance overall.
                            <br>
                            </p>
                            <p class="lead-text">
                            <b>
                            What are the benefits of using ISO 55001 to manage your assets?
                            </b>
                            <br>
                            The major benefit is of course being able to realize value from your assets, 
                            and one of the great things about this approach is that there are many quick 
                            wins early in the process. Some are related to the improved knowledge of assets, 
                            as already mentioned. In addition, the approach can help improve the relationship 
                            with stakeholders. Value doesn't necessarily mean monetary gain and defining what 
                            the value is for an asset is often a conversation that happens with people outside 
                            the company or organization.
                            </p>
                            <p class="lead-text">
                            For example, there has been a lot of interest from cities in this standard. The 
                            notion of value from a public park will not be expressed in monetary terms and 
                            defining it will mean getting closer to those using the public park. This is 
                            very beneficial for many organizations.
                            </p>
                            <p class="lead-text">
                            <b>
                            Who is this standard for?
                            </b>
                            This standard can be used by many types of organizations and companies, public 
                            or private. Everything from a city or local service provider to a supermarket 
                            chain can benefit from good asset management.
                            </p>
