                            <p class="lead-text">
                                <b>
                                What Is ISO 27001?
                                </b>
                            </p>

                            <p class="lead-text">
                            ISO/IEC 27001:2013 international standard specifies the requirements for establishing, implementing, maintaining, and continually improving an information security management system within the context of the organization. It also includes requirements for the assessment and treatment of information security risks tailored to the needs of the organization.
                            </p>

                            <p class="lead-text">
                            ISO/IEC 27001 is a security standard that formally specifies an Information Security Management System (ISMS) that is intended to bring information security under explicit management control. As a formal specification, it mandates requirements that define how to implement, monitor, maintain, and continually improve the ISMS. 
                            </p>
                            
                            <p class="lead-text">
                            Information being an asset and a building block is a key to the growth of any organization. Information needs to be suitably protected like any other important business asset.
                            </p>

                            <p class="lead-text">
                            In the modern world, this asset becomes crucial for the success and maintaining the credibility of the organization. If this asset is compromised then the organization may have to face various threats and risks like brand image erosion, business disruption, financial and productivity loss, etc. 
                            </p>

                            <p class="lead-text">
                            On the other hand, information security also maximizes return on investments, minimizes business risks, and increases business opportunities.
                            </p>

                            <p class="lead-text">
                                <b>
                                Why is ISO 27001 important?
                                </b>
                            </p>

                            <p class="lead-text">
                            Not only does the standard provide companies with the necessary know-how for protecting their most valuable information, but a company can also get certified against ISO 27001 and, in this way, prove to its customers and partners that it safeguards their data.
                            </p>

                            <p class="lead-text">
                            Individuals can also get ISO 27001-certified by attending a course and passing the exam and, in this way, prove their skills to potential employers.
                            </p>

                            <p class="lead-text">
                            Because it is an international standard, ISO 27001 is easily recognized all around the world, increasing business opportunities for organizations and professionals.
                            </p>

