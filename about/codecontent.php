                    <div id="accordContainer">
                        <button class="accordion"><span>Purpose</span></button>
                        <div class="panel">
                            <p class="lead-text">
                            The purpose of this section is to establish a Code of Ethics for S & BP 
                            Personnel and S & BP Certified Professionals.
                            </p>
                        </div>

                        <button class="accordion"><span>Responsibilities</span></button>
                        <div class="panel">
                            <p class="lead-text">
                            The S & BP Code of Conduct applies to all S & BP Professionals. This includes 
                            all certified individuals, clients, partners, trainers, auditors, employees, 
                            and other relevant stakeholders. 
                            </p>
                        </div>

                        <button class="accordion"><span>Code of Ethics</span></button>
                        <div class="panel">
                            <ul>
                                <li>
                                    <p class="lead-text">
                                    Conduct themselves professionally, with honesty, accuracy, fairness, 
                                    responsibility, and independence.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Act always solely in the best interest of their employer, their clients, the 
                                    public, and the profession by acting by the professional standards and 
                                    applicable techniques while performing professional services.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Maintain competency in their respective fields and strive to constantly improve 
                                    their professional skills.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Offer only professional services for which they are qualified to perform, and 
                                    adequately inform clients and consumers about the nature of proposed services, 
                                    including any relevant concerns or risks.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Inform each employer or client of any business interests or affiliations that 
                                    might influence their judgment or impair their fairness.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Without the proper approval of any current or previous employer or client, treat 
                                    information obtained during professional and business operations confidentially 
                                    and privately.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Comply with all laws and regulations of the jurisdictions where professional 
                                    activities are conducted.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Respect the intellectual property and contributions of others.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Not intentionally communicate false or falsified information that may 
                                    compromise the integrity of the evaluation process of a candidate for a 
                                    professional designation.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Not act in any manner that could compromise the reputation of S & BP or its 
                                    certification programs.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Fully cooperate on the inquiry following a claimed infringement of this Code of 
                                    Ethics.
                                    </p>
                                </li>
                            </ul>
                        </div>

                        <button class="accordion"><span>Sanctions for Violation of the Code of Ethics</span></button>
                        <div class="panel">
                            <ul>
                                <li>
                                    <p class="lead-text">
                                    Removal from participation in the certification program on a temporary or 
                                    permanent basis.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Revocation of certification on a temporary or permanent basis.
                                    </p>
                                </li>
                            </ul>
                        </div>

                        <button class="accordion"><span>Code of Ethics Evaluation</span></button>
                        <div class="panel">
                            <p class="lead-text">
                            Being compliant with the S & B Code of Ethics principles is essential for all 
                            S & B professionals, including S & B employees, certified individuals, trainers, 
                            partners, and other associates. Compliance is included in the performance 
                            evaluation of each stakeholder and continuous monitoring is performed regularly.
                            </p>
                            <p class="lead-text">
                            Any S & BP professional who fails to comply with the S & BP Code of Ethics shall 
                            be subject to disciplinary measures which may include the termination of their 
                            contract, and in cases when the impact is severe, legal measures will be 
                            followed. In all cases, the author of any violation shall have the right to 
                            be heard and to defend himself or herself before a disciplinary measure is 
                            imposed.
                            </p>
