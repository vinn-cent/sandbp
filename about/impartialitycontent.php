                    <div id="accordContainer">
                        <button class="accordion"><span>Purpose</span></button>
                        <div class="panel">
                            <p class="lead-text">
                            The definition of impartiality is “not prejudiced towards or against any particular side or party.” 
                            </p>

                            <p class="lead-text">
                            On the basis of that definition, S&BP seeks to attain the highest degree of public confidence and 
                            trust in rendering unbiased services. S&BP fully acknowledges the importance of impartiality 
                            in carrying out its	certification body activities that are governed by the requirements of the 
                            International Standard ISO/IEC 17024.
                            </p>
                        </div>

                        <button class="accordion"><span>Policy Statement</span></button>
                        <div class="panel">
                            <ul>
                                <li>
                                    <p class="lead-text">
                                    S&BP top management ensures that S&BP is operated in a manner to safeguard objectivity and 
                                    impartiality in delivering certification services in a non-discriminatory manner.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    S&BP policies and procedures shall be non-discriminatory and administered in a non-discriminatory manner. 
                                    Subject to limitations in this document, S&BP shall make its services available to all 
                                    applicants whose application for certification meets S&BP’s publicly available criteria 
                                    for certification offered by S&BP. Certification shall not be conditional upon education 
                                    or training from S&BP. Certification services may be restricted without discrimination where 
                                    there is an unacceptable risk S&BP. Such risks include but are not limited to evidence of 
                                    fraudulent behavior, concealment of information, or providing false information in an 
                                    application or in an assessment process.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    All S&BP personnel, contractors, and volunteers involved in the delivery of accreditation services:
                                    </p>
                                    <ul id="inner-ul">
                                        <li class="lead-text">
                                            Shall act objectively and be free from any undue commercial, financial or other 
                                            pressures that could compromise impartiality, and
                                        </li>
                                        <li class="lead-text">
                                        Are under a continual obligation to disclose potential conflicts of interest.
                                        </li>
                                    </ul>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    S&BP shall not offer or provide any service that impacts its impartiality. S&BP does not provide consultancy, 
                                    or any other services that pose a risk to impartiality. S&BP shall not suggest that certification would be simpler, 
                                    easier, faster, or less expensive if any specified person or consultancy was used.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Certification decisions shall be made by persons other than those who conducted the assessment.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    S&BP monitors conformance to this Impartiality Policy through administration of its management system, including its process for managing risks.
                                    </p>
                                </li>
                            </ul>
                        </div>

                        <button class="accordion"><span>S &amp;BP Statement of Impartiality</span></button>
                        <div class="panel">
                            <p class="lead-text">
                            S&BP understands and is fully aware of the importance of Management of impartiality in 
                            carrying out our certification activities.
                            </p>
                            <p class="lead-text">
                            S&BP has documented structures, policies and procedures to manage impartiality and to ensure that the certification activities are undertaken impartially, 
                            and has top management commitment to impartiality in certification activities.
                            </p>
                            <p class="lead-text">
                            We analyze, document and eliminate the potential conflict of interests arising from our certification activities which are structured and 
                            managed so as to safeguard impartiality.
                            </p>
                            <p class="lead-text">
                            Our aim is to inspire and prove confidence in our certification services. 
                            </p>
                        </div>

                        <button class="accordion"><span>Commitment to Impartiality</span></button>
                        <div class="panel">
                            <ul>
                                <li>
                                    <p class="lead-text">
                                    Standards and Best Practice shall commit to acting impartially in relation to its applicants, candidates, 
                                    and certified persons. Certification decisions shall be made in accordance with policies and procedures. 
                                    Policies and procedures affecting applicants, candidates, and certified persons shall be made public 
                                    and shall fairly and accurately convey information about the certification program.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Standards and Best Practice shall understand threats to impartiality that include, but are not limited to, 
                                    self‐interest, activities from related bodies, relationships of personnel, financial interests, favoritism, 
                                    conflict of interest, familiarity, and intimidation.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Standards and Best Practice shall periodically conduct a threat analysis to determine the potential, 
                                    both real and perceived, of an individual or an organization to influence certification.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Within this policy we are committed to operating our Company under the disciplines and control of Conformity 
                                    Assessment Guidelines conforming to the International Standard ISO 17024:2012
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    To ensure that the policy is successfully implemented, all employees understand the need for Impartiality and are 
                                    together responsible for the impartiality of its certification activities and shall not allow commercial, 
                                    financial or other pressures to compromise impartiality.
                                    </p>
                                </li>
                            </ul>
                        </div>
