                        <button class="accordion"><span>Purpose</span></button>
                        <div class="panel">
                            <p class="lead-text">
                            The purpose of this policy is to define the actions to be taken in the event that a complaint or appeal is received 
                            from candidates, certified persons, and other parties in regards to a certified person or decisions related to the 
                            certification process, as well as complaints for overall S&BP operations for which appeals are not applicable.
                            </p>

                            <p class="lead-text">
                            This policy excludes complaints related to alleged illegal, financial, or regulatory issues, which shall be handled 
                            by proper authorities. 
                            </p>
                        </div>

                        <button class="accordion"><span>Scope</span></button>
                        <div class="panel">
                            <b class="lead-text">The policy covers two different scopes and types of complaints (defined by S&BP) outlined as follows:</b>
                            <ul>
                                <li>
                                    <p class="lead-text">
                                    The complaints and appeals addressed to S&BP relating to a certified person 
                                    or to the certification process, i.e., activities by which a certification 
                                    body determines that a person fulfills certification requirements, including 
                                    application, assessment, the decision on certification, recertification, and 
                                    use of certificates and logos/marks, and the role of the Appeal Board in 
                                    reaching a conclusive settlement on account of an appeal. This type of 
                                    complaint is hereby referred to as “Certification Complaint.”
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    The complaints addressed to S&BP relating to overall operations of S&BP or 
                                    in other words the expression of dissatisfaction made to S&BP products or 
                                    services, procedures, policies, support, technology, representatives 
                                    (including Resellers, Trainers, or employees), overall operations, or the 
                                    complaint handling process itself, where a response or resolution is 
                                    explicitly or implicitly expected. This type of complaint is hereby referred 
                                    to as “Operations Complaint.”
                                    </p>
                                </li>
                            </ul>
                        </div>

                        <button class="accordion"><span>References</span></button>
                        <div class="panel">
                            <p class="lead-text">
                            <a href="javascript:void">ISO/IEC 17024</a>:2012, clauses 9.8 and 9.9 on Appeals and Complaints
                            </p>
                        </div>

                        <button class="accordion"><span>Terms and Definitions</span></button>
                        <div class="panel">
                            <ul>
                                <li>
                                    <p class="lead-text">
                                    <b>Complainant: </b>“person, organization or their representative making a complaint.”
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    <b>Complaint: </b>“expression of dissatisfaction, other than appeal, by any individual or organization to a 
                                    certification body [S&BP], relating to the activities of that body or a certified person, where a response is 
                                    expected.” This type of complaints is also referred as Certification Complaints within S&BP, and exclude any 
                                    operational complaints or complaints related to alleged illegal, financial, or regulatory issues, which shall be 
                                    handled by proper authorities.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    <b>Appeal: </b>“request by applicant, candidate or certified person for reconsideration of any decision made by the 
                                    certification body [S&BP] related to her/his desired certification status.”
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    <b>Certification Process: </b>“activities by which a certification body [S&BP] determines that a person fulfils 
                                    certification requirements, including application, assessment, decision on certification, recertification and use 
                                    of certificates and logos/marks.”
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    <b>Operations Complaint (S&BP term): </b>(Adapted from “Complaint” definition from <a href="javascript:void">ISO 10002</a>:2018): “expression of 
                                    dissatisfaction, made to an organization [S&BP], related to its products, services, [procedures, policies, overall 
                                    operations], or the complaint handling process itself, where a response or resolution is explicitly or implicitly 
                                    expected.” This type of complaints excludes complaints against decisions related to the certification process, 
                                    certified individuals or complaints related to alleged legal, financial, or regulatory issues, which shall be handled 
                                    by proper legal authorities. Appeals are not applicable to these types of complaints, as these complaints are not 
                                    related to any certification decisions made by S&BP.
                                    </p>
                                </li>
                            </ul>
                        </div>

                        <button class="accordion"><span>General rules for filing a complaint or an appeal</span></button>
                        <div class="panel">
                            <ul>
                                <li>
                                    <p class="lead-text">
                                    By submitting a complaint or an appeal, any complainant commits to reporting facts completely and truthfully.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Any complainant who provides any proven false statements will see their entire complaint or appeal 
                                    terminated.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    Any unsubstantiated elements-not supported with real evidences- of a complaint or an appeal 
                                    (hearsay, rumors, unproven accusations, etc.) will not be taken into considerations in any complaint or appeal.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    The Complaint and Appeal Policy does not deal with alleged illegal, financial, or regulatory issues. 
                                    For any complaint or parts of a complaint related to alleged illegal, financial, or regulatory issues, 
                                    S&BP will respond by recommending that the complainant reaches the proper authorities and inform them that 
                                    it will not deal with alleged illegal, financial, or regulatory issues. The only exception to this is if a 
                                    complainant provides evidence that a legal decision has been rendered and asks S&BP to deal with it (as an example, 
                                    if a complainant provides evidence that an S&BP employee was the subject of a recent court decision). 
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    The Complaint and Appeal Policy does not deal with issues for which the 
                                    deadlines to submit complaints and/or appeals have been reached.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    No complaint can be processed if it is done anonymously, as it must be done 
                                    and linked to a real person.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    If applicable, all complaints need to be shown to the individuals being 
                                    complained against so that they can have an opportunity to respond. If the 
                                    complainant doesn’t give the authorization for the complaint to be shown to 
                                    the person or function being complained against, the complaint cannot be 
                                    processed.
                                    </p>
                                </li>
                                <li>
                                    <p class="lead-text">
                                    If individuals exhibit querulous behavior or persistent behavior, i.e. if 
                                    they exercise their right to complain excessively or unreasonably, e.g., 
                                    excessive complaints and/or appeals within a short period, repetitive 
                                    complaints and/or appeals using the same arguments for settled issues, S&BP 
                                    will not process the complaint and/or an appeal, with prior authorization 
                                    from the Appeal Committee. 
                                    </p>
                                </li>
                            </ul>
                        </div>

                        
                        <div class="heading-wrapper with-separator">
                            <h2 class="h1">Certification <span>Complaints</span></h2>
                        </div>

                        <button class="accordion"  style="margin-top: -20%;"><span>Procedure Overview</span></button>
                        <div class="panel">
                            <p class="lead-text">
                            All complaints relating to a certified person or the certification process 
                            are reviewed by the MD and/or a designated employee assigned by him or her. 
                            The designated employee shall not be part of the staff involved in the 
                            assessment against which a certification complaint or appeal is being raised.
                            </p>

                            <p class="lead-text">
                            If the individuals filing the certification complaint are not satisfied with 
                            the review and the decision made upon the complaint, they may consider filing 
                            an appeal. 
                            </p>

                            <p class="lead-text">
                            Subsequently, S&BP appoints an Appeal Board which is in charge of the appeal 
                            process and is accountable for safeguarding the impartiality concerning the 
                            appeal. 
                            </p>

                            <p class="lead-text">
                            The Appeal Board is composed of at least three members with a majority of 
                            members being independent of S&BP personnel. Upon request of the Appeal Board, 
                            the management will provide to the board all the necessary information, 
                            including the reasons for all significant decisions, actions, and the 
                            selection of persons responsible for particular activities, to enable S&BP 
                            to ensure proper and impartial decision. 
                            </p>

                            <p class="lead-text">
                            The Appeal Board is independent of management in their recommendations except 
                            as required by international or national law. If the recommendation of the 
                            board is not respected by the management, the board shall take appropriate 
                            measures, which may include informing the accreditation body. 
                            </p>
                        </div>

                        <button class="accordion"><span>Criteria for selecting the Appeal Board Members</span></button>
                        <div class="panel">
                            <p class="lead-text">
                            The members of the Appeal Board are independent and impartial members 
                            assigned by S&BP. If a member or members of the Appeal Board have a conflict 
                            of interest, based upon the facts or circumstances of a specific appeal, 
                            including employment or other affiliations of the appellant, S&BP management 
                            in conjunction with the Appeal Board shall select a substitute member or 
                            members to hear and decide upon that claim. S&BP has the right to appoint 
                            its Impartiality Ethics and Appeals (IEA) Committee members as Appeal Board 
                            members if the criteria are met.
                            </p>
                        </div>
                        
                        <button class="accordion"><span>Operating Procedure</span></button>
                        <div class="panel">
                            <p class="lead-text">
                            When candidates disagree with the decisions made by S&BP related to their 
                            certification process, they must declare in writing, the reasons for 
                            disapproval to S&BP, and ask for a re-evaluation, via email, within 30 days 
                            from receiving S&BP’s initial decision. Re-evaluation requests received after 
                            30 days will not be processed. Re-evaluation requests are not considered 
                            complaints.
                            </p>
                            
                            <ul style="margin-top: -3.5%;">
                                <li>
                                    <p class="lead-text">
                                    <b>Certification Complaint: </b>If candidates still do not agree with the 
                                    re-evaluated decision from S&BP, they shall submit a certification complaint, 
                                    in writing through the S&BP ticketing system, no later than 30 days after 
                                    receiving the re-evaluated decision. Certification complaints received after 
                                    30 days will not be processed.
                                    </p>
                                    <p class="lead-text">
                                    Certification complaints about a certified person can be raised anytime.
                                    </p>
                                    <p class="lead-text">
                                    The certification complaint, besides the personal information (full name, 
                                    address, and other contact details) of the complainant, shall include the 
                                    complainant’s personal opinion about the assessment, the reasons for 
                                    disapproval of the decision reached during the initial decision and 
                                    re-evaluation, as well as the settlement being sought. A notice of 
                                    acknowledgment will be sent by S&BP within 24 hours following the 
                                    certification complaint reception. The reception of the complaint triggers 
                                    its initial assessment.
                                    </p>
                                    <p class="lead-text">
                                    Submission, investigation, and decision on certification complaints shall 
                                    not result in any discriminatory actions against the complainant.
                                    </p>
                                    <p class="lead-text">
                                    Certification complaints will be reviewed by the MD and/or a designated 
                                    employee assigned by him or her. The designated employee shall not be part 
                                    of the staff involved in the initial decision. The reviewer of the 
                                    certification complaint should consider the complainant’s explanation and 
                                    provide a written response, which includes:
                                    </p>
                                    <ul style="margin-top: -3.5%;">
                                        <li>
                                            <p class="lead-text">
                                            A clear explanation, or a repeated explanation of the assessment decision 
                                            following a re-evaluation of the evidence.
                                            </p>
                                        </li>
                                        <li>
                                            <p class="lead-text">
                                            The reviewer of the certification complaint should reply within 30 working 
                                            days after receiving a complaint. The complainant will receive an answer on 
                                            the decision reached in writing (via email or the ticketing system).
                                            </p>
                                        </li>
                                        <li>
                                            <p class="lead-text">
                                            If the Complainant agrees with the outcome at this stage, then the 
                                            certification complaint does not proceed to further stages.
                                            </p>
                                        </li>
                                        <li>
                                            <p class="lead-text">
                                            If Complainants are not satisfied with the outcome, then they may file 
                                            an Appeal, within 30 days from receiving the reply from S&BP 
                                            (see <a href="#appealProcess">Appeals process</a>).
                                            </p>
                                        </li>
                                        <li>
                                            <p class="lead-text">
                                            If necessary, S&BP will take appropriate corrections and corrective actions.
                                            </p>
                                        </li>
                                        <li>
                                            <p class="lead-text">
                                            All certification complaints, including actions taken, will be tracked and 
                                            recorded by S&BP.
                                            </p>
                                        </li>
                                    </ul>
                                    
