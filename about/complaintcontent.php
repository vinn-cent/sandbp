                            <h2 class="h1">Operations <span>Complaints</span></h2>
                        </div>

                        <button class="accordion" style="margin-top: -3.5%;"><span>Procedure overview</span></button>
                        <div class="panel">
                            <p class="lead-text">
                            Operations complaints and all their relevant information proceed to, upon 
                            its reception, being recorded in the ticketing system. The person wishing to 
                            make operations complaints can do so directly by submitting a service ticket. 
                            Operations complaints received via email will be recorded in the ticketing 
                            system (alongside the email) by the receiver of the complaint. Only written 
                            operations complaints will be recorded. Verbal operations complaints will 
                            have to be converted into written form. Note: When submitting a ticket, the 
                            category “Complaint” must be selected.
                            </p>
                            <p class="lead-text">
                            For the operations complaints to be handled effectively, the complaint 
                            record needs to contain all the information necessary, including the 
                            complainant’s full name, email, and phone number, the department and 
                            staff member involved in the complaint, the description of the complaint, 
                            and the date. Once recorded, the operations complaints are to be tracked 
                            through the whole complaint handling process until the final decision.
                            </p>
                            <p class="lead-text">
                            A notice of acknowledgment is sent by S&BP within 24 hours following the 
                            complaint reception. An initial assessment is conducted between 24 and 48 
                            hours following the complaint reception.
                            </p>
                        </div>

                        <button class="accordion"><span>Operations complaint Level 1</span></button>
                        <div class="panel">
                            <p class="lead-text">
                            All operations complaints initiated are considered Level 1 complaints and 
                            will be investigated and handled by the Head of the Customer Service 
                            department. The person initially addressing the operations complaints will 
                            investigate the complaint and offer a response to the complainant within 5 
                            working days of the task assignment.
                            </p>
                        </div>

                        <button class="accordion"><span>Operations complaint Level 2</span></button>
                        <div class="panel">
                            <p class="lead-text">
                            If the complainant is not satisfied with the response of the first-level 
                            investigation, the complainant has the right to file for a second-level 
                            operations complaint. The complaint is assigned to the immediate supervisor 
                            of the Customer Service department or Top Management to be investigated 
                            independently in coordination with the Internal Auditor or any other 
                            independent employee designated for the investigation.
                            </p>
                            <p class="lead-text">
                            During second-level support, the person responsible will assess the 
                            operations complaints. Based on the assessment, they will offer a response 
                            to the complainant within 30 working days. The response shall be communicated 
                            in writing along with all evidence reviewed (if applicable). 
                            </p>
                            <p class="lead-text">
                            Once the response of the second-level investigation is received by the 
                            complainant, it is considered a final response from S&BP, and at this stage, 
                            the complainant can only agree or disagree with the final response, but in 
                            either case, the process ends. In case there is a conflict of interest, real 
                            or implied, with the second-level support, e.g. between the Top Management 
                            or Internal Auditor and the complainant, an independent and external 
                            investigator, will be appointed to conduct the second-level investigation.
                            </p>
                        </div>

