                        <div class="panel">
                            <p class="lead-text">
                            <a href="https://sandbp.net/certifications/privacy">ISO/IEC 29100  </a>provides a high-level framework for protecting the 
                            Personally Identifiable Information (PII) that is within Information and Communication Technology systems (ICT). This privacy framework 
                            provided by ISO/IEC 29100 applies not only to organizations but also for persons using the ICT, which do require privacy controls in order to 
                            process the PII.
                            </p>

                            <p class="lead-text">
                            This privacy framework is intended to help organizations to define their PII related requirements for the safeguarding of privacy within an 
                            Information and Communication Technology system (ICT). This can be executed by specifying a common privacy terminology, defining the actors 
                            and their roles in processing PII and referencing familiar privacy principles.
                            </p>
                        </div>

