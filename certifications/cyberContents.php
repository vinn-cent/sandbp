                            <p class="lead-text">
                            The term <a href="https://sandbp.net/certifications/cyber-security"> ISO/IEC 27032  </a> refers to ‘Cybersecurity’ or ‘Cyberspace security,’ 
                            which is defined as the protection of privacy, integrity, and accessibility of data information in the Cyberspace. Therefore, Cyberspace is 
                            acknowledged as an interaction of persons, software and worldwide technological services.
                            </p>
                            <p class="lead-text">
                            The international standard ISO/IEC 27032 is intended to emphasize the role of different securities in the Cyberspace, regarding information 
                            security, network and internet security, and critical information infrastructure protection (CIIP). ISO/IEC 27032 as an international standard 
                            provides a policy framework to address the establishment of trustworthiness, collaboration, exchange of information, and technical guidance 
                            for system integration between stakeholders in the cyberspace.
                            </p>
