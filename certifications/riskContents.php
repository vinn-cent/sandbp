                        <div class="panel">
                            <p class="lead-text">
                            <a href="https://sandbp.net/certifications/risk-management"> ISO/IEC 31000  </a> is an international standard for Risk 
                            Management that provides a set of principles, a Risk Management framework and process, which help organizations take a proactive approach to 
                            risks that they face. ISO 31000 helps organizations to develop, implement and continuously improve a framework that aims to integrate Risk 
                            Management strategies into the overall organizational processes including decision-making.  The Risk Management process involves following 
                            five steps that identify circumstances, detect risk-associated hazards, assess, and determine the risk, control evaluated risks and review the 
                            impact of the risks.
                            </p>
                            <p class="lead-text">
                            This international standard is important if you are interested in providing yourself with comprehensive guidelines that will help your 
                            organization strengthen its indecision-making process and overall management.  ISO 31000 is intended to simplify the task of governing 
                            complex situations that require crucial decisions to be made towards a structured approach of identifying and judging risks.
                            </p>
